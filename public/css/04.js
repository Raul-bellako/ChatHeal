// Definimos una funcion SUMAR para sumar dos numeros
function sumar(a, b) {
    var resultado = a + b;
    return resultado;
  }
  
  // llamamos a la funcion, la guardamos en sumatotal y la mostramos en consola
  var num1 = 5;
  var num2 = 3;
  var sumaTotal = sumar(num1, num2);
  console.log("La suma de", num1, "y", num2, "es:", sumaTotal);
  
  