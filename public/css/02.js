// Tema2 Operadores aritméticos
var numero1 = 20;
var numero2 = 15;

var suma = numero1 + numero2;
var resta = numero1 - numero2;
var multiplicacion = numero1 * numero2;
var division = numero1 / numero2;

console.log("Suma:", suma);
console.log("Resta:", resta);
console.log("Multiplicación:", multiplicacion);
console.log("División:", division);

// tema2 Operadores de comparación
var igualdad = numero1 === numero2;
var desigualdad = numero1 !== numero2;
var mayorQue = numero1 > numero2;
var menorQue = numero1 < numero2;
var mayorIgualQue = numero1 >= numero2;
var menorIgualQue = numero1 <= numero2;

console.log("Igualdad:", igualdad);
console.log("Desigualdad:", desigualdad);
console.log("Mayor que:", mayorQue);
console.log("Menor que:", menorQue);
console.log("Mayor o igual que:", mayorIgualQue);
console.log("Menor o igual que:", menorIgualQue);

// Tema 2 Operadores lógicos
var esTrue = true;
var esFalse = false;

var andLogico = esTrue && esFalse;
var orLogico = esTrue || esFalse;
var notLogico = !esTrue;

console.log("AND lógico:", andLogico);
console.log("OR lógico:", orLogico);
console.log("NOT lógico:", notLogico);
